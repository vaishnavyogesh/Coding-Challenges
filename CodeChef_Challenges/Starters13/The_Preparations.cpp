#include<bits/stdc++.h>
using namespace std;
#define int long long
#define endl '\n'
signed main(){
    ios::sync_with_stdio(false);cin.tie(0);
    int t;
    cin>>t;
    while(t--){
        int m, n, k;
        cin>>m>>n>>k;
        int tm = n*k*1LL;
        if(tm < m)cout<<"YES"<<endl;
        else cout<<"NO"<<endl;
        
    }
}